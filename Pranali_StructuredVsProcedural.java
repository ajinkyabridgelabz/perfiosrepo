class StructuredProcedural {
    public static void main(String[] args) {
        System.out.println("Procedural Programming:");

        System.out.println("Focus: Procedural programming focuses on procedures or functions, which are self-contained blocks of code that perform specific tasks.");
        System.out.println("Control Flow: It uses a linear control flow, where instructions are executed sequentially from top to bottom. Conditional statements (if-else) and loops (for, while) are used for control flow.");
        System.out.println("Data: Data is typically organized in structures like arrays and structs. Functions manipulate these data structures.");
        System.out.println("Example: C, Pascal, and Fortran are some of the languages that primarily follow the procedural paradigm.");

        System.out.println("Structured Programming:");

        System.out.println("Focus: Structured programming is a subset of procedural programming that emphasizes the use of structured control flow constructs.");
        System.out.println("Control Flow: It promotes structured control flow constructs like loops and conditionals without the use of goto statements or unstructured jumps.");
        System.out.println("Modularity: Code is organized into modules or functions to promote reusability and maintainability.");
        System.out.println("Example: Languages like C and Pascal also support structured programming when adhering to certain coding guidelines.");
    }
}
